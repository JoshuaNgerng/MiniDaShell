#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*res;

	if (count == SIZE_MAX || size == SIZE_MAX)
		return (NULL);
	res = malloc(count * size);
	if (!res)
		return (NULL);
	ft_bzero(res, count * size);
	return (res);
}
